function RootShell({ children }: { children: ReactNode }) {
  return (
    <html lang="pt-BR">
      <head>
        <HeadContent />
      </head>
      <body>
        {children}
        <Scripts />
      </body>
    </html>
  );
}

function RootComponent() {
  const { queryClient } = Route.useRouteContext();

  return (
    <QueryClientProvider client={queryClient}>
      <Outlet />
      <Toaster
        position="top-center"
        theme="dark"
        toastOptions={{
          style: {
            background: "#181A20",
            border: "1px solid rgba(255,255,255,0.08)",
            color: "#FFFFFF",
          },
        }}
      />
    </QueryClientProvider>
  );
}
